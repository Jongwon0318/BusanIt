package day03;

public class Exam15 {
		public static void main(String[] args) {
			for(int i=1; i<=5; i++){ // 반복문 : 시작점; 끝점; 
			System.out.println(i+" : hello"+"\n");
			} //반복문에서 선언한 int i 가 유효한 구간의 끝 ( i 는 지역변수)
			for(int i=10; i>0; i--) {
				System.out.print(i+"\t");
			}//반복문에서 선언한 int i 가 유효한 구간의 끝 ( i 는 지역변수)
		}
}