package review_ex;

public class Exam03 {
	public static void main(String[] args) {
		int a=135;
		int b=20;
		System.out.println(a+"+"+b+"="+(a+b));
		System.out.println(a+"-"+b+"="+(a-b));
		System.out.println(a+"*"+b+"="+(a*b));
		System.out.println(a+"/"+b+"="+(a/b));
		System.out.println(a+"%"+b+"="+(a%b)+"\n"+"\n");
		float c=135f;
		float d=20f;
		System.out.println(c+"+"+d+"="+(c+d));
		System.out.println(c+"-"+d+"="+(c-d));
		System.out.println(c+"*"+d+"="+(c*d));
		System.out.println(c+"/"+d+"="+(c/d));
		System.out.println(c+"%"+d+"="+(c%d));
	}
}
